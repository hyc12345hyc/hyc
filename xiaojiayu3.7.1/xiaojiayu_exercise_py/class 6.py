# 请写一个程序打印出 0~100 所有的奇数
n = 0
while n <= 100:
    if n % 2 == 1:
        print(n)
    n = n + 1

#2.爱因斯坦曾出过这样一道有趣的数学题：有一个长阶梯，若每步上2阶，最后剩1阶；若每步上3阶，最后剩2阶；若每步上5阶，最后剩4阶；若每步上6阶，最后剩5阶；
# 只有每步上7阶，最后刚好一阶也不剩。（小甲鱼温馨提示：步子太大真的容易扯着蛋~~~）
# 题目：请编程求解该阶梯至少有多少阶？
a = 1
while a < 1000:
    if a % 2 == 1 and a % 3 == 2 and a % 5 == 4 and a % 6 == 5 and a % 7 == 0:
        print("楼梯有"+str(a)+"阶")
    a = a + 1